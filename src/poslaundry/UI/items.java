             cid.setText(str1);
            String name = cname.getText();
            String Address = address.getText();
            String phone = txphone.getText();
            

            pst = conn.prepareStatement("insert into customer (CID, Name, Address, Phone) values (?,?,?,?)");
            pst.setString(1, str1);
            pst.setString(2, name);
