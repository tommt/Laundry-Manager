    private void editCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editCategoryActionPerformed
        try {
            DefaultTableModel d1 = (DefaultTableModel) BrandTable.getModel();

            int selectIndex = BrandTable.getSelectedRow();
            int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

           String name = cname.getText();
            String Address = address.getText();
            String phone = txphone.getText();
         
            pst = conn.prepareStatement("update brand set CID=?, Name=?,  Address=?,  Phone=? where id=?");
            pst.setString(1, name);
            pst.setString(2, Address);
            pst.setString(3, phone);
            pst.setInt(4, id);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Hooray! Brand updated");

        } catch (SQLException ex) {
            Logger.getLogger(items.class.getName()).log(Level.SEVERE, null, ex);
        }
        tableUpdate();
         cid.setText(null);
            cname.setText(null);
            address.setText(null);
            txphone.setText(null);
    }//GEN-LAST:event_editCategoryActionPerformed

    private void deletsCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletsCategoryActionPerformed
        DefaultTableModel d1 = (DefaultTableModel) BrandTable.getModel();

        int selectIndex = BrandTable.getSelectedRow();
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to delete the Record","warning",JOptionPane.YES_NO_OPTION);
        if(dialogResult == JOptionPane.YES_OPTION){
            try {
                pst = conn.prepareStatement("delete from customer where id=?");
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Hooray! Brand Deleted");
                tableUpdate();
                 cid.setText(null);
            cname.setText(null);
            address.setText(null);
            txphone.setText(null);
            } catch (SQLException ex) {
                Logger.getLogger(items.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        updateUI();
    }//GEN-LAST:event_deletsCategoryActionPerformed

